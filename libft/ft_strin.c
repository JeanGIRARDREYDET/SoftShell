#include "libft.h"

bool	ft_strin(const char *s, const char c)
{
	size_t	i;

	i = 0;
	while (s && s[i] != 0 && s[i] != c)
	{
		i++;
	}
	if (s[i] == c && s[i] != 0)
		return (true);
	return (false);
}
